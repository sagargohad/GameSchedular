package com.cg.beans;

import java.util.List;

public class DayReport {

	private String day;
	
	private List<Game> games;
	
	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public List<Game> getGames() {
		return games;
	}

	public void setGames(List<Game> games) {
		this.games = games;
	}

	
}
