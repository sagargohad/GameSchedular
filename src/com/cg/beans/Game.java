package com.cg.beans;

public class Game {

	private String name;
	
	private byte noOfPlayers;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public byte getNoOfPlayers() {
		return noOfPlayers;
	}

	public void setNoOfPlayers(byte noOfPlayers) {
		this.noOfPlayers = noOfPlayers;
	}
	
	
}
